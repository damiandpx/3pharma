					      <a class="nav-link nav-mi" id="v-pills-messages-tab" data-toggle="pill" href="#v-pills-messagesx" role="tab" aria-controls="v-pills-messages" aria-selected="false">¿Como se aplica?</a>
							<a class="nav-link nav-mi" id="v-pills-messages-tab" data-toggle="pill" href="https://3pharma.com.mx/docs/Informacion-Tecnica-Prubeas-Moleculares-PCR.pdf" role="tab" aria-controls="v-pills-messages" aria-selected="false" data-lity data-lity-desc="Photo of a flower">Información tecnica</a>
