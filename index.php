					      <div class="tab-pane fade show active" id="v-pills-homex" role="tabpanel" aria-labelledby="v-pills-home-tab">
					      	<p>También se llaman pruebas de reacción en cadena de la polimerasa (PCR, por sus siglas en inglés) y
							son las recomendadas por la OMS para confirmar los casos vigentes de covid-19, pues detectan
							directamente el ARN (ácido ribonucleico), es decir, el material genético del virus, en las muestras
							tomadas de secreciones respiratorias del paciente.</p>
							<p>Este material genético está presente en el cuerpo antes de que se formen los anticuerpos, lo que
							significa que las pruebas moleculares pueden detectar el virus muy temprano, desde el inicio de los
							síntomas, según la Organización Panamericana de la Salud (OPS).</p>
							<p>A diferencia de las pruebas serológicas, el resultado positivo indica el que paciente está infectado en
							ese mismo momento
							</p>
					      </div>
					      <div class="tab-pane fade" id="v-pills-profilex" role="tabpanel" aria-labelledby="v-pills-profile-tab">
					      	<p>Lo que se busca en el laboratorio es saber si la muestra tiene material genético (ARN) del
							coronavirus, para eso se hace una prueba llamada reacción en cadena de la polimerasa
							de transcripción inversa. Es un poco complicado. pero básicamente lo que se hace es
							convertir el ARN viral en ADN.</p>
							<p>Posteriormente, el ADN se replica millones de veces hasta que haya suficientes copias
							para detectar utilizando un equipo especializado llamado instrumento de PCR cuantitativa.
							Se necesitan 24-72 horas para obtener los resultados de una prueba.
							</p>
					      </div>
					      <div class="tab-pane fade" id="v-pills-messagesx" role="tabpanel" aria-labelledby="v-pills-messages-tab">
							<p>Primero, se toma una muestra de la nariz o garganta del paciente, con un hisopo especial.
							Ese hisopo se coloca en un tubo y se envía a un laboratorio. Algunos hospitales grandes
							tienen laboratorios de pruebas moleculares in house, pero la mayoría de las muestras se
							envían a laboratorios externos para su procesamiento.</p>
							<p>Ese tiempo de tránsito dura unas 24 horas, pero podría ser más largo, dependiendo de qué
							tan lejos esté el hospital del laboratorio que procesa la muestra.</p>
							<p>Una vez en el laboratorio, procesar la muestra significa que los técnicos extraen el ARN
							del virus, la molécula que ayuda a regular los genes.</p>
							<p>Ese paso de limpieza, el paso de extracción de ARN, es un factor limitante, Solo los
							laboratorios más grandes tienen formas automatizadas de extraer ARN de una muestra y
							hacerlo rápidamente.</p>
							<p>Después de extraer el ARN, los técnicos deben mezclar cuidadosamente productos
							químicos especiales con cada muestra y colocarla en una máquina para su análisis, un
							proceso llamado reacción en cadena de la polimerasa (PCR), que puede detectar si la
							muestra es positiva o negativa para COVID.
							</p>
